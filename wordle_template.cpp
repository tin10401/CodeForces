void generate(string non_occur, string fixed, string potential, set<pair<char, int>> imp) {
    set<string> dict;
    ifstream fin("wordle_dict.txt");
    string w;
    while(fin >> w) {
        dict.insert(w);
    }
    const int N = 5;
    set<string> ans;
    set<char> bad(all(non_occur));
    const int M = potential.size();
    vi used(M);
    int cnt = 0;
    auto dfs = [&](auto& dfs, int i) -> void {
        if(i == 5) {
            cnt++;
            if(sum(used) == M && dict.count(fixed)) {
                ans.insert(fixed);
            }
            return;
        }
        if(fixed[i] != '?') dfs(dfs, i + 1);
        else {
            for(char ch = 'a'; ch <= 'z'; ch++) {
                if(bad.count(ch)) continue;
                if(imp.count({ch, i})) continue;
                fixed[i] = ch;
                dfs(dfs, i + 1);
                fixed[i] = '?';
            }
            for(int j = 0; j < M; j++) {
                if(!used[j]) {
                    if(imp.count({potential[j], i})) continue;
                    used[j] = true;
                    fixed[i] = potential[j];
                    dfs(dfs, i + 1);
                    used[j] = false;
                    fixed[i] = '?';
                }
            }
        }
    };
    dfs(dfs, 0);
    for(auto& x : ans) {
        cout << x << '\n';
    }
}

void solve() {
    string fixed = "??o??";
    string non_occur = "cwnbud";
    string potential = "li";
    set<pair<char, int>> imp = {{'l', 1}, {'l', 0}, {'i', 2}, {'l', 3}};
    generate(non_occur, fixed, potential, imp);
}

